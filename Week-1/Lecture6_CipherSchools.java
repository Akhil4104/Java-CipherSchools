public class CharToInt {
    public static void main(String args[]){
        char ch='a';
        int x=ch;
        System.out.println(x);

        System.out.println((int)('a'));
        System.out.println((int)('A'));

        char c3='e';
        int e=c3-'a';
        System.out.println("E is: "+e);
    }
}
